package Lesson12;

public class ArithmeticOperations {
    public static void main(String[] args) {
//Арифметические операции
        double a = 5;
        double b = 4;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
    }
}
