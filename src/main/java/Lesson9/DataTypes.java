package Lesson9;

public class DataTypes {
    //Основные типы данных
    public static void main(String[] args) {
        int i = 8;
        System.out.println(i);

        String s = "Hello World";
        System.out.println(s);

        char c = 'a';
        System.out.println(c);

        boolean b = true;
        System.out.println(b);

        double  d = 3.14;
        System.out.println(d);

    }
}
