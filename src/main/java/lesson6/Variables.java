package lesson6;

public class Variables {
    //Переменные в Java.
    public static void main(String[] args) {
        var text = "Hello world";
        System.out.println(text);
        System.out.println(text);
        System.out.println(text);
        text = "Good night";
        System.out.println(text);
        System.out.println(text);
        System.out.println(text);

    }
}
